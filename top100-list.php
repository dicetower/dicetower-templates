<p>And once again, I've decided to make my top 100 games list, just like
<a href="/top_100/2010_intro" title="View Top 100 Games from 2010" target="_blank">2010</a>, <a href="/top_100/2009_intro" title="View Top 100 Games from 2009" target="_blank">2009</a>, <a href="/top_100/intro_2008" title="View Top 100 Games from 2008" target="_blank">2008</a>, <a href="/top_100/intro-07" title="View Top 100 Games from 2007" target="_blank">2007</a>, <a href="/top_100/100intro-06" title="View Top 100 Games from 2006" target="_blank">2006</a>, and <a href="/top_100/tom_vasel_s_top_100_games" title="View Top 100 Games from 2005" target="_blank">2005</a>.</p>

<p>In 2005, I was asked to put together a list of my top 100 games for a
board game conference in Malaysia. I was a fan of top ten lists at the
time, but it never occurred to me to put together my top 100! However,
I spent the plane ride there writing down and making the list from
memory. I was fairly happy with it, and decided to post it onto the
'net to see if anyone would find it interesting.</p>

<p>Apparently enough people found it interesting, as it became the most
popular section of this website, and remains so until this day. I
decided to keep making the lists as the year went by, and three years
ago, I made two changes &ndash; went to my top 200 games, and moved the list
from December to August / September. However, 200 games was just too
much work, and so I decided to go back to 100</p>

<p>Some thoughts on the list:</p>

<p>I've played 2500+ games, which is a decent amount. The top 100 of
that list is the top 4% of games that I've played. So just because a
game is #100 on the list, does not by any means mean that I think
it's a bad game. Far from it! I think every game on this list is an
excellent game. I have not gotten rid of any of these games (of the
ones I own), and have no plans to do so in the near future.</p>

<p>Want to know how I chose the games I did? Watch my intro video!
(guys &ndash; stick the video here).</p>

<p>Some changes are major on the list, but most are minor, and just
because a game dropped down a few notches, doesn't mean I suddenly
dislike it &ndash; it's only because new games have been inserted on the
list. And even when a game dropped down a ton of notches, it doesn't
mean I dislike it, I just don't enjoy it AS much. If a game drops
completely off the list &ndash; well, then I may have had a bigger change of
mind.</p>

<p>When looking at the top 100, some interesting statistics occur.</p>

<ol>
    <li>
        <p>12 new games made my list this year. Most of those are from
this year &ndash; it's a GREAT year of games!</p>

    </li>
    <li>
        <p>36 of the games were on my first top 100 list six years ago. I
guess that means I change my mind a bit &ndash; but I'm glad that I still
hold those 36 in high regard!</p>
    </li>
    <li>
        <p>Thematic games are making an incredible rise &ndash; although some Eurogames are hanging in there!</p>

    </li>
    <li>
        <p>This is the third year for Melody doing her top 100! I gave her
complete freedom to make her list anyway that she likes &ndash; but some of
the choices she made surprised me greatly &ndash; including her # 1!! We had
15 games that we both included on our lists &ndash; which I think is a great
thing, but I'm glad they're different nonetheless. You can already see
that the "kiddy" games are starting to slide down on the list, and
some of her favorites are going to surprise you.</p>
    </li>
</ol>

<p>Well, I'm sure I'm going to get feedback on the list, and you are
certainly welcome to tell me what you think.</p>

<p>We'll post the games in groups of ten, until we hit #20, at which
point we'll slow down.</p>

<p>And this year the big news is &ndash; we'll add the People's Choice! Over
950 of you have sent in your favorite games &ndash; here we go!!!!</p>

